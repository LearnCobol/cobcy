#include "semextern.h"
#include "semcontrol.h"
#include "semdecl.h"
#include "semfile.h"
#include "symfile.h"
#include "symvar.h"

//--------------------------------------------------
ofstream 			codef;
ofstream 			declf;
vector<StackEntry*> 		SemStack;
symtable_t		 	SymTable;
int 				NestingLevel = 0;
bool				CodeBegan = false;
//--------------------------------------------------

void FinishDecl (void)
{
#ifndef NDEBUG
    cout << "DBG: Finishing declarations\n";
#endif
    CloseScopeLevels (0);
    AssociateRecordsWithFD();
    codef << "\n";
}

void StartCode (void)
{
    FinishDecl();
#ifndef NDEBUG
    cout << "DBG: Starting code, initializing variables\n";
#endif
    InitializeVariables();

#ifndef NDEBUG
    cout << "DBG: Beginning first paragraph\n";
#endif
    GenIndent();
    codef << "int _FirstParagraph (void)\n";
    codef << "{\n";
    ++ NestingLevel;

    CodeBegan = true;
}

void EndCode (void)
{
#ifndef NDEBUG
    cout << "DBG: Finished code, genrating main()\n";
#endif
    codef << "\n";
    codef << "int main (void)\n";
    codef << "{\n";
    ++ NestingLevel;

    GenIndent();
    codef << "_SetVarValues();\n\n";
    OpenSpecialFiles();		// Generates code if there are any

    GenParagraphCalls();
    CloseSpecialFiles();

    codef << "\n";
    GenIndent();
    codef << "return (0);\n";
    -- NestingLevel;
    GenIndent();
    codef << "}\n";
}

void StartProgram (void)
{
    CobolVar * NewSymbol;
    CobolFile * NewFile;
    char sourceName [PATH_MAX];
    char headerName [PATH_MAX];
    char cobolName [PATH_MAX];

    codef.open (CobcyConfig.CodeFile, ios::out | ios::trunc);
    declf.open (CobcyConfig.DeclFile, ios::out | ios::trunc);

    const char* lastSlash = strrchr (CobcyConfig.DeclFile, '/');
    if (lastSlash)
	strcpy (headerName, lastSlash+1);
    else
	strcpy (headerName, CobcyConfig.DeclFile);
    lastSlash = strrchr (CobcyConfig.CodeFile, '/');
    if (lastSlash)
	strcpy (sourceName, lastSlash+1);
    else
	strcpy (sourceName, CobcyConfig.CodeFile);
    lastSlash = strrchr (CobcyConfig.SourceFile, '/');
    if (lastSlash)
	strcpy (cobolName, lastSlash+1);
    else
	strcpy (cobolName, CobcyConfig.SourceFile);

    codef << "/* " << sourceName << "\n";
    codef << "**\n**\tGenerated by Cobol-to-C compiler from ";
    codef << cobolName << ".\n";
    codef << "*/\n\n";

    declf << "/* " << headerName << "\n";
    declf << "**\n**\tGenerated by Cobol-to-C compiler from ";
    declf << cobolName << ".\n";
    declf << "*/\n\n";

    codef << "#include <coblib/cobfunc.h>\n";	// For internal functions

    // This is the file for forward function declarations, their indices, etc.
    codef << "#include \"" << headerName << "\"\n\n";

    codef << "char _space_var [201];\n";
    codef << "long int _zero_var = 0;\n";
    codef << "long int _index = 0;\n";	// Loop iterator
    codef << "\n";

    // First paragraph's index is defined in the source file, since
    //	in the future separate compilation is possible and first paragraph
    //	is defined in every module.
    codef << "const long int _pi__FirstParagraph = 0;\n";
    // This is Current Paragraph Index
    codef << "long int _cpi;\n\n";

    SymTable.clear();

    // Space filler variable
    NewSymbol = new CobolVar;
    NewSymbol->SetPicture ("x(200)");
    NewSymbol->SetName ("_space_var");
#ifndef NDEBUG
    cout << "DBG: Declaring _space_var\n";
#endif
    SymTable["_space_var"] = NewSymbol;

    // Zero filler variable
    NewSymbol = new CobolVar;
    NewSymbol->SetPicture ("9(1)");
    NewSymbol->SetName ("_zero_var");
#ifndef NDEBUG
    cout << "DBG: Declaring _zero_var\n";
#endif
    SymTable["_zero_var"] = NewSymbol;

    // Default output stream
    NewFile = new CobolFile;
    NewFile->SetName ("stdout");
    NewFile->SetOrganization (ORG_Sequential);
    NewFile->SetAccessMode (AM_Sequential);
#ifndef NDEBUG
    cout << "DBG: Declaring stdout\n";
#endif
    SymTable["stdout"] = NewFile;
}

void EndProgram (void)
{
    codef.flush();
    codef.close();
    declf.flush();
    declf.close();
}
